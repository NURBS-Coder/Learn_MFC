	m_CButton.SetIcon(AfxGetApp()->LoadIconW(IDI_ICON1));

	//设置按钮图片2
	m_MyButton.SetWindowTextW(_T("逃跑的按钮"));
	CBitmap bitmap;
	bitmap.LoadBitmapW(IDB_BITMAP1);
	//获取位图大小
	BITMAP bmp;
	bitmap.GetBitmap(&bmp);
	//给按钮设置位图
	//m_MyButton.SetBitmap(bitmap);
